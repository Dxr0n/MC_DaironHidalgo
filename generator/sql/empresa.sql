CREATE TABLE EMPRESA (
    id_empresa INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_comercial VARCHAR(150) NOT NULL,
    razon_social VARCHAR(200) NOT NULL,
    ruc CHAR(11) UNIQUE NOT NULL,
    tipo_documento CHAR(2) DEFAULT '06',
    direccion VARCHAR(250) NOT NULL,
    codigo_ubigeo CHAR(6) NOT NULL,
    distrito VARCHAR(50) NOT NULL,
    provincia VARCHAR(50) NOT NULL,
    departamento VARCHAR(50) NOT NULL,
    pais CHAR(2) DEFAULT 'PE',
    telefono VARCHAR(20),
    correo_electronico VARCHAR(100),
    pagina_web VARCHAR(100),
    codigo_postal VARCHAR(10),
    zona_tributaria VARCHAR(50),
    regimen_tributario VARCHAR(50),
    tipo_contribuyente VARCHAR(10) DEFAULT 'JURIDICA',
    estado_empresa VARCHAR(10) DEFAULT 'ACTIVA',
    representante_legal VARCHAR(150),
    documento_representante VARCHAR(15),
    tipo_documento_representante VARCHAR(10),
    fecha_inicio_actividades DATE,
    fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    fecha_actualizacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    igv_empresa numeric(16.6)
);
